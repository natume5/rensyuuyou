data = [14, 3, 11, 133, 4]

result1 = statistics.median_low(data)
result2 = statistics.median_high(data)
result3 = statistics.median_grouped(data)
result4 = statistics.pstdev(data)
result5 = statistics.pvariance(data)
result6 = statistics.stdev(data)
result7 = statistics.variance(data)
print(result1)
print(result2)
print(result3)
print(result4)
print(result5)
print(result6)
print(result7)
