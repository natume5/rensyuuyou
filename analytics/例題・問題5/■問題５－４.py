"""
■問題５－４
（１）1から10までの整数を表示するプログラムを作れ．

（２）1から20までの整数を表示するように書き換えよ．

（３）-10から10までの整数を表示するように書き換えよ．

（４）2,4,6,8,10 を表示するように書き換えよ．

（ヒント：xを2つずつ大きくすると…）
"""


# （１）1から10までの整数を表示するプログラムを作れ．

for i in range(1, 11):
    print(i)


# （２）1から20までの整数を表示するように書き換えよ．

for i in range(1, 21):
    print(i)


# （３）-10から10までの整数を表示するように書き換えよ．

for i in range(-10, 11):
    print(i)


# （４）2,4,6,8,10 を表示するように書き換えよ．

for i in range(2, 11, 2):
    print(i)
